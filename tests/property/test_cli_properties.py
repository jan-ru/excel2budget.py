# Feature: cli-entry-point, Property 3: Invalid file bytes produce exit code 1
# ---------------------------------------------------------------------------

import argparse
import sys
import tempfile
import os
from io import StringIO

from src.cli import run


@given(raw_bytes=st.binary(min_size=0, max_size=4096))
@settings(max_examples=100, deadline=None)
def test_property_3_invalid_file_bytes_produce_exit_code_1(raw_bytes):
    """For any byte sequence that is not a valid .xlsx file, run() should
    return exit code 1 and write a parse error message to stderr.

    **Validates: Requirements 2.3, 6.2**
    """
    # Write the generated bytes to a temporary file
    fd, tmp_path = tempfile.mkstemp(suffix=".xlsx")
    try:
        os.write(fd, raw_bytes)
        os.close(fd)

        # Build a Namespace with all attributes that run() accesses
        args = argparse.Namespace(
            list_packages=False,
            list_templates=None,
            input_file=tmp_path,
            package="twinfield",
            template="budget",
            budgetcode="TEST",
            year=2025,
            output=None,
            format="csv",
            verbose=False,
            quiet=False,
        )

        # Capture stderr
        old_stderr = sys.stderr
        sys.stderr = captured = StringIO()
        try:
            exit_code = run(args)
        finally:
            sys.stderr = old_stderr

        stderr_output = captured.getvalue()

        assert exit_code == 1, (
            f"Expected exit code 1 for invalid bytes, got {exit_code}"
        )
        assert "Error:" in stderr_output, (
            f"Expected 'Error:' in stderr, got: {stderr_output!r}"
        )
    finally:
        if os.path.exists(tmp_path):
            os.remove(tmp_path)


