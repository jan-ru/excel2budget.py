"""Property tests for CLI argument parsing.

Feature: cli-entry-point, Property 1: Argument parsing round-trip

**Property 1: Argument parsing round-trip** — For any valid combination of
required arguments (input_file path, package, template, budgetcode, year) and
optional arguments (output path, format), parsing the argument list should
produce a namespace where each attribute exactly matches the provided value,
and omitted optional arguments should have their documented defaults
(format defaults to "csv", output defaults to None).

**Validates: Requirements 1.1, 1.2, 1.3, 1.4**
"""

from __future__ import annotations

from hypothesis import given, settings
from hypothesis import strategies as st

from src.cli import build_parser


# ---------------------------------------------------------------------------
# Strategies
# ---------------------------------------------------------------------------

# Positional arg strings: non-empty, no leading dash, no whitespace
_positional_str = st.text(
    alphabet=st.characters(whitelist_categories=("L", "N", "P"), blacklist_characters="-"),
    min_size=1,
    max_size=30,
).filter(lambda s: not s.startswith("-") and len(s.strip()) > 0)

# File path-like strings for input_file and output
_path_str = st.from_regex(r"[A-Za-z0-9_./]{1,40}", fullmatch=True).filter(
    lambda s: not s.startswith("-")
)


@st.composite
def cli_argv(draw: st.DrawFn):
    """Generate a valid argv list and the expected parsed values."""
    input_file = draw(_path_str)
    package = draw(_positional_str)
    template = draw(_positional_str)
    budgetcode = draw(_positional_str)
    year = draw(st.integers(min_value=-9999, max_value=9999))

    include_output = draw(st.booleans())
    include_format = draw(st.booleans())

    argv = [
        input_file,
        package,
        template,
        "--budgetcode", budgetcode,
        "--year", str(year),
    ]

    expected_output = None
    expected_format = "csv"

    if include_output:
        output_path = draw(_path_str)
        argv.extend(["--output", output_path])
        expected_output = output_path

    if include_format:
        fmt = draw(st.sampled_from(["csv", "excel"]))
        argv.extend(["--format", fmt])
        expected_format = fmt

    expected = {
        "input_file": input_file,
        "package": package,
        "template": template,
        "budgetcode": budgetcode,
        "year": year,
        "output": expected_output,
        "format": expected_format,
    }

    return argv, expected


# ---------------------------------------------------------------------------
# Property test
# ---------------------------------------------------------------------------

@given(data=cli_argv())
@settings(max_examples=100, deadline=None)
def test_property_1_argument_parsing_round_trip(data):
    """Parsing a valid argv produces a namespace matching every provided value,
    with documented defaults for omitted optional arguments.

    **Validates: Requirements 1.1, 1.2, 1.3, 1.4**
    """
    argv, expected = data
    parser = build_parser()
    ns = parser.parse_args(argv)

    # Required positional arguments (Req 1.1)
    assert ns.input_file == expected["input_file"]
    assert ns.package == expected["package"]
    assert ns.template == expected["template"]

    # Required named arguments (Req 1.2)
    assert ns.budgetcode == expected["budgetcode"]
    assert ns.year == expected["year"]

    # Optional output path (Req 1.3) — defaults to None
    assert ns.output == expected["output"]

    # Optional format (Req 1.4) — defaults to "csv"
    assert ns.format == expected["format"]


