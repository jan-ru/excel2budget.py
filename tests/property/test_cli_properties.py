from unittest.mock import patch, MagicMock

from src.templates.registry import listPackages, listTemplates

# Known registry contents
_KNOWN_PACKAGES = listPackages()          # ["twinfield", "exact", "afas"]
_KNOWN_TEMPLATES = {pkg: listTemplates(pkg) for pkg in _KNOWN_PACKAGES}

# Strategy: arbitrary strings that are NOT valid package names
_invalid_package_name = st.text(min_size=1, max_size=50).filter(
    lambda s: s not in _KNOWN_PACKAGES
)

# Strategy: pick a valid package, then generate a template name NOT in its list
@st.composite
def _valid_package_invalid_template(draw):
    pkg = draw(st.sampled_from(_KNOWN_PACKAGES))
    tpl = draw(
        st.text(min_size=1, max_size=50).filter(
            lambda s, p=pkg: s not in _KNOWN_TEMPLATES[p]
        )
    )
    return pkg, tpl


def _make_args(package: str, template: str) -> argparse.Namespace:
    """Build a minimal Namespace for run() with the given package/template."""
    return argparse.Namespace(
        list_packages=False,
        list_templates=None,
        input_file="dummy.xlsx",
        package=package,
        template=template,
        budgetcode="TEST",
        year=2025,
        output=None,
        format="csv",
        verbose=False,
        quiet=False,
    )


def _run_with_mocked_imports(args: argparse.Namespace) -> tuple[int, str]:
    """Run the CLI with file-parsing functions mocked out so execution
    reaches the template lookup step. Returns (exit_code, stderr_text)."""
    mock_workbook = MagicMock()
    mock_budget_data = MagicMock()
    mock_mapping_config = MagicMock()

    # open(...).read() returns fake bytes — mock the builtin open
    fake_file = MagicMock()
    fake_file.read.return_value = b"fake"

    with patch("src.cli.parseExcelFile", return_value=mock_workbook), \
         patch("src.cli.extractBudgetData", return_value=mock_budget_data), \
         patch("src.cli.extractMappingConfig", return_value=mock_mapping_config), \
         patch("builtins.open", return_value=fake_file):
        old_stderr = sys.stderr
        sys.stderr = captured = StringIO()
        try:
            exit_code = run(args)
        finally:
            sys.stderr = old_stderr
    return exit_code, captured.getvalue()


@given(invalid_pkg=_invalid_package_name)
@settings(max_examples=100, deadline=None)
def test_property_4a_invalid_package_lists_available_packages(invalid_pkg):
    """For any string that is not a valid accounting package name, the CLI
    should exit with code 1 and stderr should contain every available
    package name.

    **Validates: Requirements 3.2, 6.2**
    """
    args = _make_args(package=invalid_pkg, template="budget")
    exit_code, stderr_output = _run_with_mocked_imports(args)

    assert exit_code == 1, (
        f"Expected exit code 1 for invalid package '{invalid_pkg}', got {exit_code}"
    )
    for pkg in _KNOWN_PACKAGES:
        assert pkg in stderr_output, (
            f"Expected package '{pkg}' in stderr, got: {stderr_output!r}"
        )


@given(data=_valid_package_invalid_template())
@settings(max_examples=100, deadline=None)
def test_property_4b_invalid_template_lists_available_templates(data):
    """For any valid package name paired with an invalid template name,
    the CLI should exit with code 1 and stderr should contain every
    available template name for that package.

    **Validates: Requirements 3.3, 6.2**
    """
    valid_pkg, invalid_tpl = data
    args = _make_args(package=valid_pkg, template=invalid_tpl)
    exit_code, stderr_output = _run_with_mocked_imports(args)

    assert exit_code == 1, (
        f"Expected exit code 1 for invalid template '{invalid_tpl}' "
        f"in package '{valid_pkg}', got {exit_code}"
    )
    for tpl in _KNOWN_TEMPLATES[valid_pkg]:
        assert tpl in stderr_output, (
            f"Expected template '{tpl}' in stderr, got: {stderr_output!r}"
        )


